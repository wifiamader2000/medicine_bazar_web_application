const mongoose = require('mongoose');
const fs = require('fs');
const path = require('path');
require('dotenv').config({ path: path.join(__dirname, '../.env') });

const models = require('../backend/models');

const MONGO_URI = process.env.MONGO_URI || 'mongodb://localhost:27017/medicine_bazar';

const migrations = [
  { file: 'mb_users.json', model: models.User, name: 'Users' },
  { file: 'mb_products.json', model: models.Product, name: 'Products' },
  { file: 'mb_categories.json', model: models.Category, name: 'Categories' },
  { file: 'mb_orders.json', model: models.Order, name: 'Orders' },
  { file: 'mb_payments.json', model: models.Payment, name: 'Payments' },
  { file: 'mb_prescriptions.json', model: models.Prescription, name: 'Prescriptions' },
  { file: 'mb_media.json', model: models.Media, name: 'Media' },
  { file: 'mb_banners.json', model: models.Banner, name: 'Banners' },
  { file: 'mb_settings.json', model: models.Setting, name: 'Settings' },
  { file: 'mb_pos_sessions.json', model: models.POSSession, name: 'POS Sessions' },
  { file: 'mb_pos_transactions.json', model: models.POSTransaction, name: 'POS Transactions' },
  { file: 'mb_audit_logs.json', model: models.AuditLog, name: 'Audit Logs' }
];

async function migrate() {
  try {
    await mongoose.connect(MONGO_URI);
    console.log('Connected to MongoDB for migration');

    for (const migration of migrations) {
      const filePath = path.join(__dirname, '../database', migration.file);
      if (!fs.existsSync(filePath)) {
        console.log(`Skipping ${migration.name} - File not found: ${migration.file}`);
        continue;
      }

      const rawData = fs.readFileSync(filePath, 'utf8');
      const data = JSON.parse(rawData);
      
      if (!Array.isArray(data) || data.length === 0) {
        console.log(`Skipping ${migration.name} - No data found`);
        continue;
      }

      // Check if already migrated
      const count = await migration.model.countDocuments();
      if (count > 0) {
        console.log(`Skipping ${migration.name} - Already contains ${count} records`);
        continue;
      }

      // Map ID and prepare payload
      const payload = data.map(item => {
        const newItem = { ...item };
        newItem.oldId = item.id;
        delete newItem.id;
        // _id will be auto-generated by mongoose
        return newItem;
      });

      await migration.model.insertMany(payload, { ordered: false });
      console.log(`Migrated ${payload.length} ${migration.name}`);
    }

    console.log('\n--- MIGRATION SUMMARY ---');
    for (const migration of migrations) {
      const count = await migration.model.countDocuments();
      console.log(`${migration.name}: ${count}`);
    }

    await mongoose.disconnect();
    console.log('Migration completed successfully.');
  } catch (error) {
    console.error('Migration failed:', error);
    process.exit(1);
  }
}

migrate();
